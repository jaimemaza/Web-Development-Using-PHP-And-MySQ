        <h3 id="title">ISSUE</h3>


          <p id="info">More than 300 million tons of plastic are produced each year, half of which is used to create single-use items such as shopping bags, cups and straws. If disposed of improperly, plastic waste can damage the environment and biodiversity.</p>

          <br>

          <ul style="list-style-type: square; font-size:20px; margin-left: 90px;" id="list">
            <li>At least <span style="font-weight: bold;">14 million tons of plastic</span> end up in the ocean each year, and plastic makes up 80% of all marine debris.</li>
            <li>There are already more than <span style="font-weight: bold;">5 billion plastic fragments</span> floating in our oceans.</li>
            <li>Globally, <span style="font-weight: bold;">73%</span> of beach litter is plastic.</li>
            <li>By 2050, almost every species of seabird on the planet will be eating plastic.</li>
            <li><span style="font-weight: bold;">700 species</span> of marine animals have eaten or become entangled in plastic.</li>
            <li>If the data holds true, the oceans will contain <span style="font-weight: bold;">more plastic than fish</span> by 2050 according to UN data.</li>
            <li>Every second more than <span style="font-weight: bold;">200 kilos of garbage</span> ends up in the oceans.</li>
          </ul>

          <br>

          <h3 id="title">WHY IS IMPORTANT?</h3>


          <div class="container" style="border-color: #A5C6EE; width: 1090px;">

            <div style="width: 350px;"><span style="font-weight: bold;">Impacts on marine ecosystems</span><br><br>The most visible impacts of plastic debris are the ingestion, suffocation and entanglement of hundreds of marine species. Marine wildlife, such as seabirds, whales, fish and turtles, mistake plastic debris for prey; most of them die of starvation when their stomachs fill with plastic. <br></div>
            <div style="width: 350px;"><span style="font-weight: bold;">Impacts on food and human health</span><br><br>When marine organisms ingest plastic debris, these contaminants enter their digestive systems and, over time, accumulate in the food web. The transfer of contaminants between marine species and humans through seafood consumption has been identified as a health hazard.</div>
            <div style="width: 350px;"><span style="font-weight: bold;">Impacts on climate change</span><br><br>Plastic production contributes to climate change. If plastic waste is incinerated, it releases carbon dioxide and methane into the atmosphere, increasing emissions.</div>

